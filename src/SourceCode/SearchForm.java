    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {
            pst = c.prepareStatement("select * from agridata where crope_name = ?");
            String cropName = searchField.getText();
            pst.setString(1, cropName);
            ResultSet rs = pst.executeQuery();
            if(rs.next() == true) {
                cropField.setText(rs.getString("crope_name"));
                phField.setText(rs.getString("soil_ph"));
                soilField.setText(rs.getString("soil_type"));
                tempField.setText(rs.getString("temp"));
                rainField.setText(rs.getString("rainfall"));
                sunField.setText(rs.getString("sunlight"));
                periodField.setText(rs.getString("period"));
            } else {
                ImageIcon icon = new ImageIcon("src/images/notFound.png");
                JOptionPane.showMessageDialog(null, "Crop's name is invalid !", "Sorry â¹ï¸", JOptionPane.INFORMATION_MESSAGE,icon);
                cropField.setText("");
                phField.setText("");
                soilField.setText("");
                tempField.setText("");
                rainField.setText("");
                sunField.setText("");
                periodField.setText("");
                searchField.requestFocus();
            }
        } catch(Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

